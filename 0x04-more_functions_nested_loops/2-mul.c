#include "main.h"

/**
 * mul - Multiplies two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Return: The product of a and b
 */
int mul(int a, int b)
{
	/**
	 * The product of two integers is calculated using the '*' operator.
	 * Return the result of the multiplication.
	 */
	return (a * b);
}
